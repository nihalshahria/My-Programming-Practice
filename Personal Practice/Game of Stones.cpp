#include <stdio.h>
#define sf(a)           scanf("%d",&a)
int a[101];
void play(){
    a[0]=a[7]=a[1]=0;
    a[2]=a[3]=a[5]=a[4]=a[6]=1;
    for(int i = 8; i<101; i++){
        if(a[i-2]==0 || a[i-3]==0 || a[i-5]==0) a[i]=1;
        else a[i]=0;
    }
}
int main(){
    play();
    int t,n;
    sf(t);
    while(t--){
        sf(n);
        printf(a[n]?"First\n":"Second\n");
    }
    return 0;
}
